<h3 align="center"> Steps: </h3>

<p>
<ol data-role="listview">
<li><p>1.Sit on the Padmasana (Lotus Pose) with eye closed.</p></li>
<li><p>2.Close your eyes and breathe deeply.</p></li>
<li><p>3.Now close your ears lids or flaps with your thumbs.</p></li>
<li><p>4.Place your index finger just above your eyebrows and the rest of your fingers over your eyes with your middle fingers.</p></li>
<li><p>5.Applying very gentle pressure to the sides of your nose.</p></li>
<li><p>6.Now concentrate your mind on the area between your eyebrows.</p></li>
<li><p>7.Keep your mouth closed; breathe out slowly through your nose with making a humming sound of Om.</p></li>
<li><p>8.Repeat this process for 5 times. Important thing is that while doing this Pranayama assume that your are being connected to all the positive energies of the universe.</p></li></ol>

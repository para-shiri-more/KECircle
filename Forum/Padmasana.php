<h3 align="center"> Steps: </h3>

<p> <ol data-role="listview">
<li>1.Sit on the floor and stretch your legs, your legs should be straight in front. Hold the right leg in both the hands, fold the legs slowly and place it on your left thigh. Ensure that your feet should touch your navel.<br><br></li>
<li>2.Same like earlier fold the left knee, and hold it with both hands and place it on the right thigh close to the other. At this point your both knees should touch the floor and the foot should face upwardly.<br><br></li>
<li>3.Your spinal cord should be straight at this point. If you feel some difficulty while sitting in the posture for a long time, you can change the legs and then sit on the same position.<br><br></li>
<li>4.Your spinal cord should be erect, your both hands should be together or put the palms facing upside on the knee joints and the thumb must touch your index finger and the other fingers should face the upward.</li></ol>
